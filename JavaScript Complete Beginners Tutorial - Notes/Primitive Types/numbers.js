// in JS we have Primitive Types:
/*

    *   Number
    *   String
    *   Boolean
    *   Null
    *   Undefined

*/

// --- --- --->

// Number value in JS has a max value as well (it can't go on infinitely)
// JS can do simple calculations with numbers (+ - * / %)
// we also have 'exponential operator', which look like this: **

// exponential operator **
2 ** 3
// return 8
// it works like that: 2 * 2 * 2 = 8

5 ** 2
// return 25
// it works like that: 5 * 5 = 25

5 ** 5
// return 3125
// it works like that: 5 * 5 * 5 * 5 * 5 = 3125

// --- --- --->

// NaN examples (NaN is still a number on it's own)

0/0
// return NaN

1 + NaN
// return NaN

// --- --- --->

// Infinity example

1/0
// return Infinity

// Negative Infinity example

-1/0
// return -Infinity

// --- --- --->

// Quiz

// 1. What does this evaluate to ??
1.5 + 1.5 * 2

// answer = 4.5

// 2. What does this evaluate to ??
(10 % 6) ** 2

// answer = 16

// 3. What does this evaluate to ??
200 * 0 / 0

